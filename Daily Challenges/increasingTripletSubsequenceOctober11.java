public class increasingTripletSubsequenceOctober11 {

  public static void main(String[] args) {
    int[] arr = { 1, 5, 0, 4, 1, 3 };
    // int[] arr = { 5, 4, 3, 2, 1 };
    System.out.println(increasingTriplet(arr));
  }

  public static boolean increasingTriplet(int[] nums) {
    int len = nums.length;
    if (len <= 2) {
      return false;
    }

    return false;
  }
}
